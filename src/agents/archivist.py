class TraceLogger:
    """Append-only JSONL audit log of agent actions."""

    def __init__(self, path: Path) -> None:
        self.path = path
        self.path.parent.mkdir(parents=True, exist_ok=True)

    def log(
        self,
        agent: str,
        action: str,
        details: dict[str, Any] | None = None,
        confidence: str = "high",
        source: str = "static_analysis",
    ) -> None:
        """Append one trace entry.

        Args:
            agent: Name of the agent emitting this entry.
            action: Short description of the action taken.
            details: Optional structured details (file paths, counts, etc.).
            confidence: "high" | "medium" | "low"
            source: "static_analysis" | "llm_inference" | "git_analysis"
        """
        entry = {
            "timestamp": datetime.now(timezone.utc).isoformat(),
            "agent": agent,
            "action": action,
            "confidence": confidence,
            "evidence_source": source,
            "details": details or {},
        }
        with self.path.open("a", encoding="utf-8") as f:
            f.write(json.dumps(entry) + "\n")


