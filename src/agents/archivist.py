def generate_onboarding_brief(
    day_one_answers: dict[str, str],
    module_graph: ModuleGraph,
    lineage_graph: LineageGraph,
) -> str:
    """Generate onboarding_brief.md from Day-One answers and graph data."""
    top = _top_modules_by_pagerank(module_graph, n=5)
    sources = lineage_graph.find_sources()[:5]
    sinks = lineage_graph.find_sinks()[:5]
    G = module_graph.graph

    lines = [
        "# FDE Day-One Onboarding Brief\n",
        f"\n_Generated by Brownfield Cartographer — {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}_\n",
        "\n---\n",
        "## The Five FDE Day-One Questions\n",
    ]

    for i, (question, answer) in enumerate(day_one_answers.items(), start=1):
        lines.append(f"\n### Q{i}: {question}\n\n{answer}\n")

    lines += [
        "\n---\n",
        "## Quick-Reference Architecture\n",
        "\n**Top modules (by architectural centrality):**\n",
    ]
    for m in top:
        attrs = G.nodes.get(m["path"], {})
        meta_parts = [f"pagerank={m.get('pagerank', 0):.4f}"]
        if attrs.get("line_count"):
            meta_parts.append(f"lines 1–{attrs['line_count']}")
        if attrs.get("complexity_score") is not None:
            meta_parts.append(f"complexity={attrs['complexity_score']:.1f}")
        if attrs.get("change_velocity_30d"):
            meta_parts.append(f"{attrs['change_velocity_30d']} commits/30d")
        lines.append(f"- `{m['path']}` ({', '.join(meta_parts)})\n")
        if attrs.get("purpose_statement"):
            lines.append(f"  _{attrs['purpose_statement'][:120]}_\n")

    lines.append("\n**Data entry points:**\n")
    for s in sources:
        attrs = G.nodes.get(s, {})
        loc = f" — `{attrs.get('source_file', s)}`" if attrs.get("source_file") else ""
        lines.append(f"- `{s}`{loc}\n")
    if not sources:
        lines.append("- _(none detected)_\n")

    lines.append("\n**Data output endpoints:**\n")
    for s in sinks:
        attrs = G.nodes.get(s, {})
        loc = f" — `{attrs.get('source_file', s)}`" if attrs.get("source_file") else ""
        lines.append(f"- `{s}`{loc}\n")
    if not sinks:
        lines.append("- _(none detected)_\n")

    # Evidence citations: top modules with available line-range metadata
    lines += ["\n---\n", "## Evidence: Module Citation Index\n"]
    evidence_modules = _top_modules_by_pagerank(module_graph, n=10)
    for m in evidence_modules:
        path = m["path"]
        attrs = G.nodes.get(path, {})
        evidence_parts: list[str] = []
        if attrs.get("line_count"):
            evidence_parts.append(f"lines 1–{attrs['line_count']}")
        if attrs.get("complexity_score") is not None:
            evidence_parts.append(f"complexity={attrs['complexity_score']:.1f}")
        if attrs.get("change_velocity_30d"):
            evidence_parts.append(f"{attrs['change_velocity_30d']} commits/30d")
        if attrs.get("domain_cluster"):
            evidence_parts.append(f"domain={attrs['domain_cluster']}")
        evidence_str = ", ".join(evidence_parts) if evidence_parts else "no metadata"
        lines.append(f"- `{path}` ({evidence_str})\n")

    return "".join(lines)


