def archive(
    module_graph: ModuleGraph,
    lineage_graph: LineageGraph,
    semantic_results: dict[str, Any] | None = None,
    *,
    output_dir: str | Path = ".cartography",
    config: LLMConfig | None = None,
    config_path: str | Path | None = None,
    trace_logger: TraceLogger | None = None,
) -> dict[str, str]:
    """Run the Archivist: write CODEBASE.md, onboarding_brief.md, cartography_trace.jsonl.

    Returns:
        Dict with keys: codebase_md_path, onboarding_brief_path, trace_path
    """
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    if config is None:
        config = load_config(config_path)

    # Escalate to cloud model for large repos (same threshold as Semanticist)
    python_modules = [
        n for n in module_graph.graph.nodes()
        if module_graph.graph.nodes[n].get("node_type") == "module"
        and module_graph.graph.nodes[n].get("language") == "python"
    ]
    if len(python_modules) > config.large_repo_threshold and config.cloud_model:
        print(
            f"[Archivist] Large repo ({len(python_modules)} Python modules > {config.large_repo_threshold})"
            f" — escalating to {config.cloud_model} ...",
            flush=True,
        )
        config = build_cloud_config(config)

    trace_path = output_dir / "cartography_trace.jsonl"
    if trace_logger is None:
        trace_logger = TraceLogger(trace_path)

    print("[Archivist] Generating CODEBASE.md ...", flush=True)
    trace_logger.log(
        "Archivist", "generate_codebase_md",
        details={"output": str(output_dir / "CODEBASE.md")},
        source="llm_inference",
    )
    codebase_md = generate_codebase_md(
        module_graph, lineage_graph,
        semantic_results=semantic_results,
        config=config,
    )
    codebase_md_path = output_dir / "CODEBASE.md"
    codebase_md_path.write_text(codebase_md, encoding="utf-8")

    print("[Archivist] Generating onboarding_brief.md ...", flush=True)
    day_one_answers = (semantic_results or {}).get("day_one_answers", {})
    if not day_one_answers:
        day_one_answers = {"(no semantic analysis run)": "Run with --llm to generate Day-One answers."}

    trace_logger.log(
        "Archivist", "generate_onboarding_brief",
        details={"output": str(output_dir / "onboarding_brief.md"), "answers_count": len(day_one_answers)},
        source="llm_inference",
    )
    brief_md = generate_onboarding_brief(day_one_answers, module_graph, lineage_graph)
    brief_path = output_dir / "onboarding_brief.md"
    brief_path.write_text(brief_md, encoding="utf-8")

    trace_logger.log(
        "Archivist", "archive_complete",
        details={
            "codebase_md": str(codebase_md_path),
            "onboarding_brief": str(brief_path),
            "trace": str(trace_path),
        },
        confidence="high",
        source="static_analysis",
    )

    G = module_graph.graph
    G_lin = lineage_graph.graph
    print(
        f"[Archivist] Done: CODEBASE.md ({len(codebase_md)} chars), "
        f"onboarding_brief.md, trace log → {output_dir}",
        flush=True,
    )

    return {
        "codebase_md_path": str(codebase_md_path),
        "onboarding_brief_path": str(brief_path),
        "trace_path": str(trace_path),
    }
