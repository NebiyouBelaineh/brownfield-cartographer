def _top_modules_by_pagerank(module_graph: ModuleGraph, n: int = 5) -> list[dict[str, Any]]:
    G = module_graph.graph
    ranked = sorted(
        (
            (node, G.nodes[node])
            for node in G.nodes()
            if G.nodes[node].get("node_type") == "module"
        ),
        key=lambda x: x[1].get("pagerank", 0.0),
        reverse=True,
    )
    return [{"path": p, **attrs} for p, attrs in ranked[:n]]


def _high_velocity_files(module_graph: ModuleGraph, n: int = 10) -> list[dict[str, Any]]:
    G = module_graph.graph
    ranked = sorted(
        (
            (node, G.nodes[node])
            for node in G.nodes()
            if G.nodes[node].get("node_type") == "module"
        ),
        key=lambda x: x[1].get("change_velocity_30d", 0),
        reverse=True,
    )
    return [
        {"path": p, "commits": attrs.get("change_velocity_30d", 0)}
        for p, attrs in ranked[:n]
        if attrs.get("change_velocity_30d", 0) > 0
    ]


def _circular_deps(module_graph: ModuleGraph) -> list[list[str]]:
    import networkx as nx
    G = module_graph.graph
    return [list(c) for c in nx.strongly_connected_components(G) if len(c) > 1]


def _domain_summary(module_graph: ModuleGraph) -> dict[str, list[str]]:
    G = module_graph.graph
    domains: dict[str, list[str]] = {}
    for node in G.nodes():
        if G.nodes[node].get("node_type") != "module":
            continue
        domain = G.nodes[node].get("domain_cluster") or "unknown"
        domains.setdefault(domain, []).append(node)
    return domains


def _doc_drift_modules(module_graph: ModuleGraph) -> list[dict[str, str]]:
    G = module_graph.graph
    drifts = []
    for node in G.nodes():
        if G.nodes[node].get("doc_drift"):
            drifts.append({"path": node, "summary": G.nodes[node]["doc_drift"]})
    return drifts


def generate_codebase_md(
    module_graph: ModuleGraph,
    lineage_graph: LineageGraph,
    semantic_results: dict[str, Any] | None = None,
    *,
    config: LLMConfig | None = None,
    config_path: str | Path | None = None,
) -> str:
    """Generate the CODEBASE.md living context file content.

    The architecture overview paragraph is generated by the LLM.
    All other sections are derived from graph data (static analysis).
    """
    if config is None:
        config = load_config(config_path)

    G_mod = module_graph.graph
    G_lin = lineage_graph.graph

    top_modules = _top_modules_by_pagerank(module_graph, n=5)
    hot_files = _high_velocity_files(module_graph, n=10)
    circular = _circular_deps(module_graph)
    domains = _domain_summary(module_graph)
    drift_modules = _doc_drift_modules(module_graph)
    sources = lineage_graph.find_sources()[:10]
    sinks = lineage_graph.find_sinks()[:10]

    # LLM-generated architecture overview
    module_list = "\n".join(
        f"- {m['path']} (pagerank={m.get('pagerank', 0):.4f}, "
        f"lang={m.get('language', '?')}, "
        f"purpose={m.get('purpose_statement', 'unknown')[:120]})"
        for m in top_modules
    )
    messages = [
        {
            "role": "system",
            "content": (
                "You write concise architectural overviews for software engineers. "
                "Given a list of the most important modules in a codebase, write a single "
                "paragraph (3-5 sentences) describing the system's architecture. "
                "Be specific about what the system does, its key components, and data flow direction."
            ),
        },
        {
            "role": "user",
            "content": (
                f"Top modules by architectural importance (PageRank):\n{module_list}\n\n"
                f"Total modules: {G_mod.number_of_nodes()}\n"
                f"Total lineage nodes: {G_lin.number_of_nodes()}\n"
                f"Data sources: {', '.join(sources[:5]) or 'none detected'}\n"
                f"Data sinks: {', '.join(sinks[:5]) or 'none detected'}\n\n"
                "Write the architecture overview paragraph."
            ),
        },
    ]
    try:
        arch_overview = chat_completion(messages, config=config, max_tokens=256).strip()
    except Exception as exc:
        arch_overview = f"[LLM error generating overview: {exc}]"

    # Build the markdown sections
    lines: list[str] = [
        "# CODEBASE.md — Living Context File",
        f"\n_Generated by Brownfield Cartographer on {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}_\n",
        "---\n",
        "## Architecture Overview\n",
        arch_overview,
        "\n---\n",
        "## Critical Path (Top 5 Modules by PageRank)\n",
    ]
    for m in top_modules:
        purpose = m.get("purpose_statement") or "_(no purpose statement)_"
        lines.append(
            f"- **`{m['path']}`** (pagerank={m.get('pagerank', 0):.4f})  \n"
            f"  {purpose}\n"
        )

    lines += ["\n---\n", "## Data Sources & Sinks\n", "**Sources (entry points):**\n"]
    for s in sources:
        lines.append(f"- `{s}`\n")
    if not sources:
        lines.append("- _(none detected)_\n")

    lines.append("\n**Sinks (output endpoints):**\n")
    for s in sinks:
        lines.append(f"- `{s}`\n")
    if not sinks:
        lines.append("- _(none detected)_\n")

    lines += ["\n---\n", "## Domain Architecture Map\n"]
    for domain, mods in sorted(domains.items()):
        lines.append(f"**{domain.capitalize()}** ({len(mods)} modules):\n")
        for mod in sorted(mods)[:8]:
            lines.append(f"- `{mod}`\n")
        if len(mods) > 8:
            lines.append(f"- _…and {len(mods) - 8} more_\n")
        lines.append("\n")

    lines += ["\n---\n", "## Known Debt\n"]
    if circular:
        lines.append("**Circular dependencies:**\n")
        for cycle in circular[:5]:
            lines.append(f"- {' → '.join(cycle[:6])}{'…' if len(cycle) > 6 else ''}\n")
    else:
        lines.append("- No circular dependencies detected.\n")

    if drift_modules:
        lines.append("\n**Documentation drift (docstring contradicts implementation):**\n")
        for d in drift_modules[:10]:
            lines.append(f"- `{d['path']}`: {d['summary']}\n")
    else:
        lines.append("\n- No documentation drift detected.\n")

    lines += ["\n---\n", "## High-Velocity Files (Pain Points)\n"]
    if hot_files:
        for f in hot_files:
            lines.append(f"- `{f['path']}` — {f['commits']} commits (last 30d)\n")
    else:
        lines.append("- _(git velocity data unavailable — not a git repo or no recent commits)_\n")

    lines += ["\n---\n", "## Module Purpose Index\n"]
    for node in sorted(G_mod.nodes()):
        if G_mod.nodes[node].get("node_type") != "module":
            continue
        purpose = G_mod.nodes[node].get("purpose_statement")
        if purpose:
            lines.append(f"- **`{node}`**: {purpose}\n")

    return "".join(lines)


